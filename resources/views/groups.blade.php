    <h1>Move Groups</h1>

    <p>We are thrilled that you are interested in Move Groups and taking the gospel and love of Christ to your campus!</p>

    <p>Move groups is our way of taking the Church outside of the walls of a building and into our communities. As a student, God can use you to encourage and reach people that a pastor couldn’t. You can connect with people before they ever connect to Church, and we believe God will use you and others like you to bring hope to your schools and communities.</p>

    <h3>What is a move group?</h3>
    <p>A Move Group is simply a group of 3 or more students who consistently connect with God and each other. We don’t beat people up with Bibles. We just love and hang out with them. Move Group leaders will encourage their groups each week using a verse or two, but there are no sermons or lectures.</p>

    <p>A Move Group is just a group of people loving each other because Jesus loved them. When someone who doesn’t know Jesus experiences the love and community of a Move Group, it will change their life!</p>

    <h3>How to start a move group:</h3>
    <p>There are 5 steps to starting a move group.</p>
    <ol>
        <li>
            <a href="https://fpstudents.wufoo.com/forms/move-group-registration/" target="_blank">Sign-up Here</a>: When you sign up, we will contact you soon with next steps.
        </li>
        <li>Watch the &quot;Welcome to Move Group&quot; Video below.</li>
        <li>Find a place in your school where you can meet consistently.</li>
        <li>Invite people to join you.</li>
        <li>Choose your content. Check out the suggested material below.</li>
    </ol>

    <p>You can also sign up to
        <a href="https://fpstudents.wufoo.com/forms/move-group-registration/" target="_blank">join an existing move group</a> without leading one.
    </p>
    <p>The Church works best when we go outside the walls and love people where they are. When students take the love of Christ with them where they go, the world will change. Will you join the Movement?</p>

